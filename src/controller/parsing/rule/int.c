#include "miniRT.h"

int	int_rule(char *n)
{
	int	i;

	i = 0;
	if (n[0] == '-' || n[0] == '+')
		i = 1;
	if (!(n[i]))
		return (1);
	while (n[i])
	{
		if (!ft_isdigit(n[i]))
			return (1);
		i++;
	}
	return (0);
}