#include "miniRT.h"

void	matrix_3x3_transpose(t_vector m[3])
{
	m[0] = (t_vector){m[0].x, m[1].x, m[2].x};
	m[1] = (t_vector){m[0].y, m[1].y, m[2].y};
	m[2] = (t_vector){m[0].z, m[1].z, m[2].z};
}